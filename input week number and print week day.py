week=int(input ("Enter the no. of day week:"))
if (week==1):
    print("1=Monday.")
elif (2==week):
    print("2=Tuesday")
elif (week==3):
    print("3=Wednesday")
elif (week==4):
    print("4=Thursday")
elif (week==5):
    print("5=Friday")
elif (week==6):
    print("6=saturday")
else:
    print("7=sunday")